public class MultiplicationTables {
    public static void main(String[] args) {
        for (int i = 6; i <= 9; i++) {
            System.out.println("Multiplication table for " + i + ":");
            for (int j = 1; j <= 10; j++) {
                System.out.println(i + " * " + j + " = " + (i * j));
            }
        }
    }

   

    
}
